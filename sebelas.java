public class sebelas {
    public static void main(String[] args) {
        int j;
        int k;
        int l=5;


        for(j=1;j<=l;j++){
            if(j%2==0){
                System.out.print(" ");
            }
            for (k=1;k<=l;k++){
                System.out.print("#");
            }
            System.out.print("\n");
        }
    }
}
